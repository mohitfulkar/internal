import math 
import random 
import datetime 
# Math module 
print("Math module:") 
print("Square root of 16:", math.sqrt(16)) 
print("Ceil of 4.3:", math.ceil(4.3)) 
print("Factorial of 5:", math.factorial(5)) 
# Random module 
print("\nRandom module:") 
print("Random number between 0 and 1:", random.random()) 
print("Random integer between 1 and 100:", random.randint(1, 100)) 
print("Random choice from a list:", random.choice(['apple', 'banana', 'orange'])) 
# Datetime module 
print("\nDatetime module:") 
current_time = datetime.datetime.now() 
print("Current date and time:", current_time) 
print("Current year:", current_time.year) 
print("Current month:", current_time.month) 
print("Current day:", current_time.day) 